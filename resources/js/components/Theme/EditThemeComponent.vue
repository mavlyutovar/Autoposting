<script>
import ShowTheme from "./ShowThemeComponent";

export default {
    data:function(){
        return{
            theme: [],
        }
    },
    mounted() {
        this.update();
    },
        components: {
            ShowTheme
        },
    methods: {
        update: function() {
            this.percent = 0;
            axios.post('/get-all-theme', {
                text: this.percent,
                //description: this.description
            }).then((response) => {
                this.theme = response.data;
                console.log(this.theme)
            });

        },
        deleteTheme: function(id) {
            axios.post('/delete-theme/'+id).then((response) => {
                console.log(id);
                this.theme = response.data
                this.update();
            });
        },
        editTheme: function(id) {
            axios.post('/edit-theme/'+id).then((response) => {
                this.theme = response.data
                this.$emit('updButton', {
                    theme: this.theme,
                    urlPicture: this.urlPicture
                })
            });
        },
        showTheme: function(id) {
            this.$refs.updateShow.update(id);
        },
        duplicateTheme: function(id) {
            axios.post('/duplicate-theme/'+id).then((response) => {
                this.theme = response.data
            });
        },
    }
}
</script>

